-- ตรวจสอบเฉพาะวัคซีนพิษสุนัขบ้า
SELECT
    vaccine_name,
    vaccine_type,
    total_doses,
    dose_intervals,
    CASE
        WHEN dose_intervals::text = '[3, 7, 14, 28]' THEN '✅ ถูกต้อง'
        WHEN dose_intervals::text = '[3,7,14,28]' THEN '✅ ถูกต้อง'
        WHEN dose_intervals::text = '[3, 4, 7, 14]' THEN '❌ ผิด (ค่าเก่า)'
        WHEN dose_intervals::text = '[3,4,7,14]' THEN '❌ ผิด (ค่าเก่า)'
        ELSE '⚠️ ค่าไม่รู้จัก'
    END as status,
    updated_at
FROM vaccine_schedules
WHERE vaccine_type = 'rabies';

