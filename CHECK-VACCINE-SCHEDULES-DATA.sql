-- ตรวจสอบข้อมูลวัคซีนทั้งหมด
SELECT
    id,
    vaccine_name,
    vaccine_type,
    total_doses,
    dose_intervals,
    active,
    updated_at
FROM vaccine_schedules
ORDER BY vaccine_name;

